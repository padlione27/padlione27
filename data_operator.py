# Contoh penggunaan operator aritmatika
a = 10
b = 3

# Penjumlahan
penjumlahan = a + b
print("Penjumlahan:", penjumlahan)  # Output: 13

# Pengurangan
pengurangan = a - b
print("Pengurangan:", pengurangan)  # Output: 7

# Perkalian
perkalian = a * b
print("Perkalian:", perkalian)      # Output: 30

# Pembagian
pembagian = a / b
print("Pembagian:", pembagian)      # Output: 3.3333333333333335

# Modulus
modulus = a % b
print("Modulus:", modulus)          # Output: 1

# Eksponen
eksponen = a ** b
print("Eksponen:", eksponen)        # Output: 1000

# Pembagian bulat
pembagian_bulat = a // b
print("Pembagian Bulat:", pembagian_bulat)  # Output: 3